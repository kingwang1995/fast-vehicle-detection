def nms(boxes, scores, iou_threshold, max_output_size, soft_nms=False):
    keep = []
    order = scores.argsort()[::-1]  # 按得分从大到小排序
    num = boxes.shape[0]
    suppressed = np.zeros((num), dtype=np.int)  # 抑制
    for _i in range(num):
        if len(keep) >= max_output_size:
            break
        i = order[_i]
        if suppressed[i] == 1:
            continue
        keep.append(i)
        ####boxes左上和右下角坐标####
        xi1 = boxes[i, 0]
        yi1 = boxes[i, 1]
        xi2 = boxes[i, 2]
        yi2 = boxes[i, 3]
        areas1 = (xi2 - xi1 + 1) * (yi2 - yi1 + 1)  # box1面积
        for _j in range(_i + 1, num):  # start，stop
            j = order[_j]
            if suppressed[i] == 1:
                continue
            xj1 = boxes[j, 0]
            yj1 = boxes[j, 1]
            xj2 = boxes[j, 2]
            yj2 = boxes[j, 3]
            areas2 = (xj2 - xj1 + 1) * (yj2 - yj1 + 1)  # box2面积

            xx1 = np.maximum(xi1, xj1)
            yy1 = np.maximum(yi1, yj1)
            xx2 = np.minimum(xi2, xj2)
            yy2 = np.minimum(yi2, yj2)

            w = np.maximum(0.0, xx2 - xx1 + 1)
            h = np.maximum(0.0, yy2 - yy1 + 1)

            int_area = w * h  # 重叠区域面积

            inter = 0.0

            if int_area > 0:
                inter = int_area * 1.0 / (areas1 + areas2 - int_area)  # IOU
            ###softnms
            if soft_nms:
                sigma = 0.6
                if inter >= iou_threshold:
                    scores[j] = np.exp(-(inter * inter) / sigma) * scores[j]
            ###nms
            else:
                if inter >= iou_threshold:
                    suppressed[j] = 1
    return keep  # 返回保留下来的下标


def build_targets(pred_boxes, pred_cls, target, anchors, ignore_thres):
    ByteTensor = torch.cuda.ByteTensor if pred_boxes.is_cuda else torch.ByteTensor
    FloatTensor = torch.cuda.FloatTensor if pred_boxes.is_cuda else torch.FloatTensor

    nB = pred_boxes.size(0)  # num_samples, num_anchors, grid_size, grid_size, 4
    nA = pred_boxes.size(1)
    nC = pred_cls.size(-1)  # num_samples, num_anchors, grid_size, grid_size, num_classes
    nG = pred_boxes.size(2)

    # Output tensors
    obj_mask = ByteTensor(nB, nA, nG, nG).fill_(0)
    noobj_mask = ByteTensor(nB, nA, nG, nG).fill_(1)
    class_mask = FloatTensor(nB, nA, nG, nG).fill_(0)
    iou_scores = FloatTensor(nB, nA, nG, nG).fill_(0)
    tx = FloatTensor(nB, nA, nG, nG).fill_(0)
    ty = FloatTensor(nB, nA, nG, nG).fill_(0)
    tw = FloatTensor(nB, nA, nG, nG).fill_(0)
    th = FloatTensor(nB, nA, nG, nG).fill_(0)
    tcls = FloatTensor(nB, nA, nG, nG, nC).fill_(0)

    # Convert to position relative to box
    target_boxes = target[:, 2:6] * nG  # target的1维是其对应图片的idx
    gxy = target_boxes[:, :2]
    gwh = target_boxes[:, 2:]
    # Get anchors with best iou
    ious = torch.stack([bbox_wh_iou(anchor, gwh) for anchor in anchors])  # anchors*target
    best_ious, best_n = ious.max(0)
    # Separate target values
    b, target_labels = target[:, :2].long().t()  # target = [idx, labels, x, y, w, h]
    gx, gy = gxy.t()
    gw, gh = gwh.t()
    gi, gj = gxy.long().t()  # long是变成长整型
    # Set masks
    obj_mask[b, best_n, gj, gi] = 1
    noobj_mask[b, best_n, gj, gi] = 0

    # Set noobj mask to zero where iou exceeds ignore threshold
    for i, anchor_ious in enumerate(ious.t()):
        noobj_mask[b[i], anchor_ious > ignore_thres, gj[i], gi[i]] = 0

    # Coordinates
    tx[b, best_n, gj, gi] = gx - gx.floor()
    ty[b, best_n, gj, gi] = gy - gy.floor()
    # Width and height
    tw[b, best_n, gj, gi] = torch.log(gw / anchors[best_n][:, 0] + 1e-16)
    th[b, best_n, gj, gi] = torch.log(gh / anchors[best_n][:, 1] + 1e-16)
    # One-hot encoding of label
    tcls[b, best_n, gj, gi, target_labels] = 1
    # Compute label correctness and iou at best anchor
    class_mask[b, best_n, gj, gi] = (pred_cls[b, best_n, gj, gi].argmax(-1) == target_labels).float()
    iou_scores[b, best_n, gj, gi] = bbox_iou(pred_boxes[b, best_n, gj, gi], target_boxes, x1y1x2y2=False)

    tconf = obj_mask.float()
    return iou_scores, class_mask, obj_mask, noobj_mask, tx, ty, tw, th, tcls, tconf
